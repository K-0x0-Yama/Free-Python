# 文字列(Strings)

# ダブルクオーテーションを使った文字列作成
print("Hell world!!")

# シングルクオーテーションを使った文字列作成
print('Hell world!!')

# 文字列作成の中にクォーテーションマークを入れたい場合
# ダブルで囲むならシングルを使用
print("I'm fine!!")

# シングルで囲むならダブルを使用
print('I"m fine!!')

# シングルで囲むならダブルを使用
print('I"m fine!!')

# 文字列認識させるためにバックスラッシュを使う
print('I\'m fine')


# 複数行で文字列を作成したいとき
print("""
引用符
3つで
囲む
""")

# 文字列のなかで改行させたいとき
print("hell \nworld!!")
